import { toast } from 'react-toastify'

export const ShowSuccessToast = (message: string) => {

  toast.success(message, {
    position: 'top-right',
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    theme: 'light'
  }) 
}

